package org.senju.eshopeule.exceptions;

public class PermissionNotExistsException extends Exception {
    public PermissionNotExistsException() {super();}
    public PermissionNotExistsException(String message) {super(message);}
    public PermissionNotExistsException(String message, Throwable cause) {super(message, cause);}
    public PermissionNotExistsException(Throwable cause) {super(cause);}
}
