package org.senju.eshopeule.exceptions;

public class RoleNotExistsException extends Exception {
    public RoleNotExistsException() {super();}
    public RoleNotExistsException(String message) {super(message);}
    public RoleNotExistsException(String message, Throwable cause) {super(message, cause);}
    public RoleNotExistsException(Throwable cause) {super(cause);}
}
